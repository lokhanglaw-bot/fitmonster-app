const translations: Record<Language, Translations> = { en, zh };

// ─── Helper to interpolate {key} placeholders ─────────────────
function interpolate(template: string, vars?: Record<string, string | number>): string {
  if (!vars) return template;
  return template.replace(/\{(\w+)\}/g, (_, key) => String(vars[key] ?? `{${key}}`));
}

// ─── Context ──────────────────────────────────────────────────
interface I18nContextValue {
  language: Language;
  setLanguage: (lang: Language) => void;
  t: Translations;
  /** Interpolate a translated string with variables */
  tr: (key: string, vars?: Record<string, string | number>) => string;
}

const I18nContext = createContext<I18nContextValue>({
  language: "en",
  setLanguage: () => {},
  t: en,
  tr: (key) => key,
});

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = useState<Language>("en");
  const [loaded, setLoaded] = useState(false);

  useEffect(() => {
    AsyncStorage.getItem(STORAGE_KEY).then((val) => {
      if (val === "zh" || val === "en") setLanguageState(val);
      setLoaded(true);
    });
  }, []);

  const setLanguage = useCallback((lang: Language) => {
    setLanguageState(lang);
    AsyncStorage.setItem(STORAGE_KEY, lang);
  }, []);

  const t = translations[language];

  const tr = useCallback(
    (key: string, vars?: Record<string, string | number>) => {
      // Navigate nested keys like "homeTabs.home"
      const parts = key.split(".");
      let val: any = t;
      for (const p of parts) {
        val = val?.[p];
      }
      if (typeof val === "string") return interpolate(val, vars);
      return key;
    },
    [t],
  );

  if (!loaded) return null;

  return (
    <I18nContext.Provider value={{ language, setLanguage, t, tr }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  return useContext(I18nContext);
}
